-- 1º Conceito de trigger
/*
Associar um gatilho a um evento
Ex: ao inserir um dado na tabela acionar envento em outro lugar
Diferente da procedure, a trigger é evocada automaticamente. Tudo asociado a um evento.
Obs: Não se coloca lógica de negócio a uma trigger.
Trigger é pra manter a integridade dos dados.

*/